TEMPLATES = {
    "local": "run_templates/local_run.yaml.template",
    "slurm": "run_templates/hpc_slurm_run_v2.yaml.template",
}


def offer_plan_or_run(config_path: Path) -> int:
    rel = config_path.relative_to(ROOT)
    action = ask_choice(
        "What now?",
        [
            ("1", "Preview the plan (safe, no compute)"),
            ("2", "Run the pipeline now (live progress)"),
            ("3", "Just save the config and exit"),
        ],
        default="1",
    )
    if action == "1":
        print()
        return subprocess.call(
            [sys.executable, str(ROOT / "scripts" / "vermamg.py"), "plan", "--config", str(config_path)],
            cwd=str(ROOT),
        )
    if action == "2":
        return run_with_progress(config_path)
    print(f"\n  Saved. Later you can run it with:\n"
          f"    python scripts/vermamg_init.py --run {rel}        # live stage progress\n"
          f"    python scripts/vermamg.py run --config {rel} --resume --follow\n")
    return 0


def interactive_flow() -> int:
    a = questionnaire()
    cfg_dir = ROOT / "run_configs"
    cfg_dir.mkdir(exist_ok=True)
    config_path = cfg_dir / f"{a['project_slug']}_{a['run_label']}.yaml"
    config_path.write_text(build_config_text(a), encoding="utf-8")
    print(f"\n  Wrote config: {config_path.relative_to(ROOT)}")
    return offer_plan_or_run(config_path)


def template_flow() -> int:
    env = ask_choice(
        "Which environment is the template for?",
        [("1", "Local machine / WSL"), ("2", "HPC cluster (SLURM)")],
        default="1",
    )
    tpl = ROOT / TEMPLATES["local" if env == "1" else "slurm"]
    if not tpl.is_file():
        print(f"  ! Template not found: {tpl}")
        return 1
    name = slugify(ask("Config file name (without .yaml)", "my_project_run"))
    cfg_dir = ROOT / "run_configs"
    cfg_dir.mkdir(exist_ok=True)
    config_path = cfg_dir / f"{name}.yaml"
    if config_path.exists() and ask(f"{config_path.name} exists. Overwrite? (y/N)", "n").lower() not in ("y", "yes"):
        print("  Aborted (existing file kept).")
        return 0
    config_path.write_text(tpl.read_text(encoding="utf-8"), encoding="utf-8")
    rel = config_path.relative_to(ROOT)
    print(f"\n  Wrote a template to: {rel}")
    print("  Next:")
    print(f"    1) Open {rel} in your editor and fill every [FILL] field.")
    print("    2) Then run it (either way):")
    print(f"         python scripts/vermamg_init.py --run {rel}        # live stage progress")
    print(f"         python scripts/vermamg.py run --config {rel} --resume --follow")
    return 0


def main(argv: list[str] | None = None) -> int:
    parser = argparse.ArgumentParser(
        description="VermAMG setup wizard / launcher with live stage progress.")
    parser.add_argument("--run", metavar="CONFIG",
                        help="Run an existing config file with live stage progress (skip the questions).")
    args = parser.parse_args(argv)

    if args.run:
        config_path = Path(args.run)
        if not config_path.is_absolute():
            config_path = ROOT / config_path
        if not config_path.is_file():
            print(f"  ! Config not found: {args.run}")
            return 2
        return run_with_progress(config_path)

    how = ask_choice(
        "How would you like to configure VermAMG?",
        [
            ("1", "Interactive  - answer a few questions here in the terminal"),
            ("2", "Template     - write a config file to fill in your editor, then run"),
        ],
        default="1",
    )
    return interactive_flow() if how == "1" else template_flow()


if __name__ == "__main__":
    raise SystemExit(main())
