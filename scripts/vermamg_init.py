#!/usr/bin/env python3
"""
VermAMG interactive setup wizard.

Run this after cloning to create a run config by answering a few questions,
then optionally launch the pipeline with a clean, high-level live progress view
(one line per stage - not the full logs).

    python scripts/vermamg_init.py

No flags required. Everything is asked interactively.
"""
from __future__ import annotations

import argparse
import re
import subprocess
import sys
import time
from pathlib import Path

ROOT = Path(__file__).resolve().parents[1]
sys.path.insert(0, str(ROOT / "scripts"))

# ---------------------------------------------------------------------------
# Small input helpers
# ---------------------------------------------------------------------------

def ask(prompt: str, default: str = "") -> str:
    suffix = f" [{default}]" if default else ""
    try:
        val = input(f"  {prompt}{suffix}: ").strip()
    except EOFError:
        val = ""
    return val or default


def ask_choice(prompt: str, options: list[tuple[str, str]], default: str) -> str:
    print(f"\n  {prompt}")
    for key, label in options:
        print(f"     {key}) {label}")
    valid = {k for k, _ in options}
    while True:
        val = ask("Choose", default)
        if val in valid:
            return val
        print(f"     ! Please choose one of: {', '.join(sorted(valid))}")


def slugify(text: str) -> str:
    s = re.sub(r"[^A-Za-z0-9]+", "_", text.strip().lower()).strip("_")
    return s or "project"


def warn_if_missing(path: str, label: str) -> None:
    if path and not (Path(path).is_absolute() and Path(path).exists()
                     or (ROOT / path).exists()):
        print(f"     ! note: {label} not found yet ({path}) - make sure it exists before running.")


