def questionnaire() -> dict:
    print("=" * 70)
    print("  VermAMG setup wizard")
    print("  Answer a few questions to generate a run config. Press Enter to")
    print("  accept the [default] shown in brackets.")
    print("=" * 70)

    a: dict = {}

    src = ask_choice(
        "What input do you want to run?",
        [
            ("1", "Bundled smoke demo (3 proteins, no data needed - great first run)"),
            ("2", "My own data, precomputed (I already have PDBs + Foldseek hits)"),
            ("3", "My own data, live (run ColabFold + Foldseek; needs GPU + databases)"),
        ],
        default="1",
    )
    a["data_source"] = {"1": "demo", "2": "precomputed", "3": "live"}[src]

    print()
    a["project_name"] = ask("Project name", "VermAMG Demo" if a["data_source"] == "demo" else "My VermAMG Project")
    a["project_slug"] = slugify(ask("Project slug (folder name)", slugify(a["project_name"])))
    a["run_label"] = ask("Run label (run identifier)",
                         "smoke_demo_v1" if a["data_source"] == "demo" else "run_v1")

    a["mode"] = "full"

    env = ask_choice(
        "Where will this run?",
        [("1", "Local machine / WSL (local_wsl)"), ("2", "HPC cluster (SLURM)")],
        default="1",
    )
    a["backend"] = "local" if env == "1" else "slurm"
    a["profile"] = "local_wsl" if env == "1" else "slurm"

    a["slurm_account"] = a["slurm_cpu"] = a["slurm_gpu"] = ""
    if a["backend"] == "slurm":
        a["slurm_account"] = ask("SLURM account", "")
        a["slurm_cpu"] = ask("SLURM CPU partition", "")
        a["slurm_gpu"] = ask("SLURM GPU partition", "")

    a["threads"] = ask("CPU threads for Foldseek/P2Rank", "4")

    # defaults
    a.update({
        "metadata_tsv": "", "colabfold_cmd": "colabfold_batch",
        "foldseek_bin": "", "pdb_db": "", "afsp_db": "",
        "java_bin": "java",
        "p2rank_cmd": "resources/tools/p2rank/current/prank",
        "p2rank_jar": "resources/tools/p2rank/current/bin/p2rank.jar",
    })

    if a["data_source"] == "demo":
        a.update(DEMO)
        print("\n  Using bundled smoke-demo data (no paths to enter).")
    elif a["data_source"] == "precomputed":
        print("\n  Point the wizard at YOUR precomputed inputs:")
        a["fasta"] = ask("FASTA file path", "inputs/my_proteins.faa")
        a["query_pdb_dir"] = ask("Directory of per-protein PDB files", "inputs/query_pdbs")
        a["collector_manifest"] = ask("Collector manifest TSV (run_id|pdb_path|confidence_score)", "inputs/collector_manifest.tsv")
        a["pdb_all_hits"] = ask("Foldseek-vs-PDB hits TSV", "inputs/pdb_all_hits.tsv")
        a["afsp_all_hits"] = ask("Foldseek-vs-AFSP hits TSV", "inputs/afsp_all_hits.tsv")
        a["id_map"] = ask("ID map TSV (run_id -> protein_id)", "inputs/id_map.tsv")
        a["ref_source_run_root"] = ask("Prior run root with materialized references "
                                       "(or use the demo cache)", DEMO["ref_source_run_root"])
        a["metadata_tsv"] = ask("Metadata TSV (optional)", "")
        a["p2rank_cmd"] = ask("P2Rank command", a["p2rank_cmd"])
        a["p2rank_jar"] = ask("P2Rank jar", a["p2rank_jar"])
        a["java_bin"] = ask("java binary", a["java_bin"])
        for p, lbl in [(a["fasta"], "FASTA"), (a["query_pdb_dir"], "query_pdb_dir"),
                       (a["collector_manifest"], "collector_manifest")]:
            warn_if_missing(p, lbl)
    else:  # live
        print("\n  Live mode runs ColabFold + Foldseek from your FASTA (needs GPU + DBs):")
        a["fasta"] = ask("FASTA file path", "inputs/my_proteins.faa")
        a["query_pdb_dir"] = ""
        a["collector_manifest"] = ""
        a["pdb_all_hits"] = a["afsp_all_hits"] = a["id_map"] = ""
        a["ref_source_run_root"] = ask("Prior run root with materialized references (optional)", "")
        a["colabfold_cmd"] = ask("ColabFold command", a["colabfold_cmd"])
        a["foldseek_bin"] = ask("Foldseek binary", "resources/tools/foldseek/bin/foldseek")
        a["pdb_db"] = ask("Foldseek PDB database", "resources/databases/foldseek/pdb/pdb")
        a["afsp_db"] = ask("Foldseek AFSP database", "resources/databases/foldseek/alphafold_swissprot/af_swissprot")
        a["p2rank_cmd"] = ask("P2Rank command", a["p2rank_cmd"])
        a["p2rank_jar"] = ask("P2Rank jar", a["p2rank_jar"])
        warn_if_missing(a["fasta"], "FASTA")

    return a


