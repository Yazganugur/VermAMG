# Live high-level progress (polls state/status.json - no verbose logs)
# ---------------------------------------------------------------------------

def run_with_progress(config_path: Path) -> int:
    from vermamg_lib.config import load_run_config
    from vermamg_lib.run_context import RunContext
    from vermamg_lib.stage_registry import build_stages
    from vermamg_lib.state import read_status

    cfg = load_run_config(config_path)
    ctx = RunContext.from_config(config_path, cfg)
    stages = [s for s in build_stages(ctx) if s.enabled(ctx)]
    order = [s.stage_id for s in stages]
    titles = {s.stage_id: s.title for s in stages}
    total = len(stages)

    print("\n" + "=" * 70)
    print(f"  Running: {ctx.project_name} / {ctx.run_label}   ({total} stages)")
    print(f"  Run dir: {ctx.rel_to_project(ctx.run_root)}")
    print("  Live progress (one line per stage; full logs in the run dir):")
    print("=" * 70)

    done: set[str] = set()
    running_shown: str | None = None

    def mark_done_through(stage_id: str) -> None:
        if stage_id not in order:
            return
        for sid in order[: order.index(stage_id) + 1]:
            if sid not in done:
                done.add(sid)
                print(f"  [OK] [{len(done):>2}/{total}] {titles.get(sid, sid)}")

    log_dir = ROOT / "logs"
    log_dir.mkdir(exist_ok=True)
    run_log = log_dir / f"wizard_run_{ctx.run_label}.log"

    with run_log.open("w", encoding="utf-8") as logf:
        proc = subprocess.Popen(
            [sys.executable, str(ROOT / "scripts" / "vermamg.py"),
             "run", "--config", str(config_path), "--resume"],
            cwd=str(ROOT), stdout=logf, stderr=subprocess.STDOUT,
        )
        while True:
            st = read_status(ctx.run_root) or {}
            lps = st.get("last_pass_stage", "")
            cur = st.get("current_stage", "")
            if lps:
                mark_done_through(lps)
            if cur and cur not in done and cur != running_shown:
                running_shown = cur
                print(f"  >> [{len(done) + 1:>2}/{total}] {titles.get(cur, cur)} ...", flush=True)
            if proc.poll() is not None:
                break
            time.sleep(0.6)

    st = read_status(ctx.run_root) or {}
    if lps := st.get("last_pass_stage", ""):
        mark_done_through(lps)
    final = st.get("status", "UNKNOWN")
    print("=" * 70)
    if proc.returncode == 0:
        print(f"  [OK] DONE - {final}.  Exports under: {ctx.rel_to_project(ctx.run_root)}/exports/")
    else:
        cur = st.get("current_stage", "")
        where = titles.get(cur, cur) if cur else "setup / validation (before the first stage)"
        print(f"  [X] STOPPED at: {where}  (status={final})")
        for err in (st.get("validation_errors") or [])[:6]:
            print(f"    - {err}")
        markers = ("RUNTIME_ENVIRONMENT_ERROR", "MISSING_STAGE_INPUT", "RUN_EXECUTION_ERROR",
                   "CONFIG_ERROR", "EXCEPTION")
        try:
            lines = run_log.read_text(encoding="utf-8", errors="replace").splitlines()
        except OSError:
            lines = []
        for ln in [l for l in lines if any(m in l for m in markers)][-8:]:
            print(f"    | {ln.strip()[:120]}")
        print(f"    Full run log: {run_log.relative_to(ROOT)}")
        if st.get("latest_log"):
            print(f"    Stage log:    {st['latest_log']}")
        print("    Tip: fix the issue and re-run - it resumes from the last PASS.")
    print("=" * 70)
    return proc.returncode


